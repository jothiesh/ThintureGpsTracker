    private boolean isValidGpsCoordinatesEnhanced(double latitude, double longitude) {
        // Basic range check
        if (latitude < -90.0 || latitude > 90.0) {
            logColorful("‚ùå Invalid latitude: " + latitude + " (range: -90 to 90)", RED);
            return false;
        }
        
        if (longitude < -180.0 || longitude > 180.0) {
            logColorful("‚ùå Invalid longitude: " + longitude + " (range: -180 to 180)", RED);
            return false;
        }
        
        // Check for null island (0,0) - usually invalid for GPS tracking
        if (Math.abs(latitude) < 0.001 && Math.abs(longitude) < 0.001) {
            logColorful("‚ö†Ô∏è Null Island coordinates detected: " + latitude + ", " + longitude, YELLOW);
            return false;
        }
        
        // Check for common test coordinates
        if ((Math.abs(latitude - 12.9716) < 0.0001 && Math.abs(longitude - 77.5946) < 0.0001)) {
            logColorful("‚ö†Ô∏è Test coordinates detected (Bangalore): " + latitude + ", " + longitude, YELLOW);
            // Return true for test coordinates in development
            return true;
        }
        
        return true;
    }
