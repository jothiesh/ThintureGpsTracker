    /**
     * üî® Creates a new MQTT client with enhanced configuration
     */
    public MqttClient createClient(MqttCallback callback) throws MqttException {
        long startTime = System.currentTimeMillis();
        totalConnectionAttempts.incrementAndGet();
        
        try {
            // üÜî Generate unique client ID
            String clientId = generateUniqueClientId();
            
            logColorful("üî® CREATING NEW MQTT CLIENT", BRIGHT_BLUE);
            logColorful("üì± Client ID: " + clientId, CYAN);
            logColorful("‚è±Ô∏è Timestamp: " + LocalDateTime.now().format(timeFormatter), PURPLE);
            
            // üîß Create client with memory persistence
            MqttClient client = new MqttClient(brokerUrl, clientId, new MemoryPersistence());
            
            // üîÑ Set callback
            client.setCallback(callback);
            
            // üìä Track connection info
            ConnectionInfo info = new ConnectionInfo(clientId, System.currentTimeMillis());
            activeConnections.put(clientId, info);
            currentConnections.incrementAndGet();
            
            long creationTime = System.currentTimeMillis() - startTime;
            logColorful("‚úÖ CLIENT CREATED SUCCESSFULLY in " + creationTime + "ms", BRIGHT_GREEN);
            
            return client;
            
        } catch (MqttException e) {
            failedConnections.incrementAndGet();
            logColorful("‚ùå CLIENT CREATION FAILED: " + e.getMessage(), BRIGHT_RED);
            throw e;
        }
    }
    
    /**
     * üîó Connects the MQTT client with smart retry logic
     */
    public void connect(MqttClient client) throws MqttException {
        String clientId = client.getClientId();
        logColorful("üîó CONNECTING CLIENT: " + clientId, BRIGHT_YELLOW);
        
        MqttException lastException = null;
        long totalStartTime = System.currentTimeMillis();
        
        // üîÑ Retry loop with exponential backoff
        for (int attempt = 1; attempt <= MAX_RETRY_ATTEMPTS; attempt++) {
            try {
                long attemptStartTime = System.currentTimeMillis();
                
                // üîß Create optimized connection options
                MqttConnectOptions options = createOptimizedConnectOptions();
                
                logColorful("üîÑ Connection attempt #" + attempt + " for " + clientId, YELLOW);
                displayConnectionAttempt(attempt, clientId);
                
                // üîå Attempt connection
                client.connect(options);
                
                // ‚úÖ Connection successful
                long connectionTime = System.currentTimeMillis() - attemptStartTime;
                long totalTime = System.currentTimeMillis() - totalStartTime;
                
                successfulConnections.incrementAndGet();
                totalConnectionTime.addAndGet(totalTime);
                
                // üìä Update connection info
                ConnectionInfo info = activeConnections.get(clientId);
                if (info != null) {
                    info.connectedAt = System.currentTimeMillis();
                    info.connectionTime = connectionTime;
                    info.attempts = attempt;
                }
                
                displayConnectionSuccess(clientId, attempt, connectionTime, totalTime);
                return;
                
            } catch (MqttException e) {
                lastException = e;
                failedConnections.incrementAndGet();
                
                logColorful("‚ùå Attempt #" + attempt + " failed: " + e.getMessage(), RED);
                
                // üîÑ Don't retry on certain errors
                if (isNonRetryableError(e)) {
                    logColorful("üö´ Non-retryable error, stopping attempts", BRIGHT_RED);
                    throw e;
                }
                
                // üò¥ Wait before retry (except last attempt)
                if (attempt < MAX_RETRY_ATTEMPTS) {
                    long delay = calculateRetryDelay(attempt);
                    logColorful("üò¥ Waiting " + delay + "ms before retry #" + (attempt + 1), YELLOW);
                    
                    try {
                        Thread.sleep(delay);
                    } catch (InterruptedException ie) {
                        Thread.currentThread().interrupt();
                        logColorful("üîÑ Retry interrupted", YELLOW);
                        throw new MqttException(MqttException.REASON_CODE_CLIENT_EXCEPTION, ie);
                    }
                } else {
                    logColorful("üîö MAX RETRY ATTEMPTS REACHED", BRIGHT_RED);
                }
            }
        }
        
        // üí• All attempts failed
        long totalTime = System.currentTimeMillis() - totalStartTime;
        displayConnectionFailure(clientId, MAX_RETRY_ATTEMPTS, totalTime, lastException);
        throw lastException;
    }
    
    /**
     * üîß Create optimized connection options for production
     */
    private MqttConnectOptions createOptimizedConnectOptions() {
        MqttConnectOptions options = new MqttConnectOptions();
        
        // üîê Authentication
        options.setUserName(username);
        options.setPassword(password.toCharArray());
        
        // üîÑ Connection behavior
        options.setCleanSession(true);
        options.setAutomaticReconnect(true);
        options.setConnectionTimeout(connectionTimeout);
        options.setKeepAliveInterval(keepAliveInterval);
        options.setMaxInflight(maxInflight);
        
        // üöÄ Performance optimizations
        options.setMaxReconnectDelay(10000); // 10 seconds max
        options.setServerURIs(new String[]{brokerUrl}); // Single server for now
        
        // üîí Security settings
        options.setHttpsHostnameVerificationEnabled(false);
        options.setSocketFactory(null); // Use default
        
        logColorful("üîß Connection options configured", CYAN);
        return options;
    }
    
    /**
     * üìä Calculate retry delay with exponential backoff and jitter
     */
    private long calculateRetryDelay(int attempt) {
        long baseDelay = (long) (INITIAL_RETRY_DELAY * Math.pow(RETRY_MULTIPLIER, attempt - 1));
        long clampedDelay = Math.min(baseDelay, MAX_RETRY_DELAY);
        
        // üé≤ Add jitter to prevent thundering herd
        double jitter = 1.0 + (ThreadLocalRandom.current().nextDouble() - 0.5) * 2 * JITTER_FACTOR;
        return (long) (clampedDelay * jitter);
    }
    
    /**
     * üö´ Check if error is non-retryable
     */
    private boolean isNonRetryableError(MqttException e) {
        int reasonCode = e.getReasonCode();
        return reasonCode == MqttException.REASON_CODE_INVALID_CLIENT_ID ||
               reasonCode == MqttException.REASON_CODE_NOT_AUTHORIZED ||
               reasonCode == MqttException.REASON_CODE_FAILED_AUTHENTICATION ||
               reasonCode == MqttException.REASON_CODE_INVALID_PROTOCOL_VERSION;
    }
    
    /**
     * üîå Disconnect client safely
     */
    public void disconnect(MqttClient client) {
        if (client == null) return;
        
        String clientId = client.getClientId();
        logColorful("üîå DISCONNECTING CLIENT: " + clientId, BRIGHT_YELLOW);
        
        try {
            long startTime = System.currentTimeMillis();
            
            // üîÑ Disconnect with timeout
            if (client.isConnected()) {
                client.disconnect(5000); // 5 second timeout
                
                long disconnectTime = System.currentTimeMillis() - startTime;
                logColorful("‚úÖ CLIENT DISCONNECTED in " + disconnectTime + "ms", BRIGHT_GREEN);
            } else {
                logColorful("‚ö†Ô∏è Client was already disconnected", YELLOW);
            }
            
        } catch (MqttException e) {
            logColorful("‚ùå DISCONNECT ERROR: " + e.getMessage(), BRIGHT_RED);
        } finally {
            // üßπ Clean up resources
            try {
                client.close();
                logColorful("üßπ Client resources cleaned up", CYAN);
            } catch (MqttException e) {
                logColorful("‚ö†Ô∏è Error cleaning up client resources: " + e.getMessage(), YELLOW);
            }
            
            // üìä Update tracking
            activeConnections.remove(clientId);
            currentConnections.decrementAndGet();
        }
    }
    
    /**
     * üì° Subscribe to topics with enhanced error handling
     */
    public void subscribe(MqttClient client, String[] topics) throws MqttException {
        if (client == null || topics == null || topics.length == 0) {
            logColorful("‚ö†Ô∏è Invalid parameters for subscription", YELLOW);
            return;
        }
        
        String clientId = client.getClientId();
        logColorful("üì° SUBSCRIBING TO TOPICS", BRIGHT_BLUE);
        logColorful("üì± Client: " + clientId, CYAN);
        logColorful("üìä Topic count: " + topics.length, CYAN);
        
        if (!client.isConnected()) {
            logColorful("‚ùå CLIENT NOT CONNECTED", BRIGHT_RED);
            throw new MqttException(MqttException.REASON_CODE_CLIENT_NOT_CONNECTED);
        }
        
        try {
            long startTime = System.currentTimeMillis();
            
            // üîß Prepare QoS levels (using QoS 1 for performance)
            int[] qosLevels = new int[topics.length];
            Arrays.fill(qosLevels, 1);
            
            // üì° Subscribe to all topics
            client.subscribe(topics, qosLevels);
            
            long subscriptionTime = System.currentTimeMillis() - startTime;
            
            // üìä Update connection info
            ConnectionInfo info = activeConnections.get(clientId);
            if (info != null) {
                info.subscribedTopics = Arrays.asList(topics);
                info.subscriptionTime = subscriptionTime;
            }
            
            // üé® Display subscription success
            displaySubscriptionSuccess(clientId, topics, subscriptionTime);
            
        } catch (MqttException e) {
            logColorful("‚ùå SUBSCRIPTION FAILED: " + e.getMessage(), BRIGHT_RED);
            throw e;
        }
    }
    
    /**
     * üîç Validate broker URL
     */
    private boolean isValidBrokerUrl(String url) {
        if (url == null || url.trim().isEmpty()) {
            return false;
        }
        
        try {
            URI uri = new URI(url);
            String scheme = uri.getScheme();
            String host = uri.getHost();
            int port = uri.getPort();
            
            // üîç Check required components
            if (scheme == null || host == null || port <= 0) {
                return false;
            }
            
            // üîç Check supported schemes
            if (!scheme.equals("tcp") && !scheme.equals("ssl") && !scheme.equals("ws") && !scheme.equals("wss")) {
                return false;
            }
            
            logColorful("‚úÖ Broker URL validation passed", GREEN);
            logColorful("üîó Scheme: " + scheme, CYAN);
            logColorful("üåê Host: " + host, CYAN);
            logColorful("üîå Port: " + port, CYAN);
            
            return true;
            
        } catch (URISyntaxException e) {
            logColorful("‚ùå Invalid broker URL syntax: " + e.getMessage(), RED);
            return false;
        }
    }
    
    /**
     * üÜî Generate unique client ID
     */
    private String generateUniqueClientId() {
        String timestamp = String.valueOf(System.currentTimeMillis());
        String random = UUID.randomUUID().toString().substring(0, 8);
        String threadId = String.valueOf(Thread.currentThread().getId());
        
        return baseClientId + "-" + timestamp.substring(timestamp.length() - 6) + "-" + random + "-" + threadId;
    }
    
